alphabet = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h']

print("First three letters:", alphabet[:3])

middle_index = len(alphabet) // 2
print("Three letters from the middle:", alphabet[middle_index:middle_index+3])

start_index = 4
print("Letters from index", start_index, "to the end:", alphabet[start_index:])
